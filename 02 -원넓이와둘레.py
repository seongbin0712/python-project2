# 1. 프로그램 제목, 설명은 적당히 표현
# 2. 입력 : 반지름
# 3. 원 넓이 계산 : 반지름 * 반지름 * 3.14
# 4. 원의 둘레 계산 : 2 * 반지름 * 3.14
# 5. 원 넓이와 둘레 값을 출력

# 힌트 : 반지름의 변수명은 r
# 원의 면적의 변수 명 : area
# 원의 둘레 변수명은 : circleference
# 반지름은 정수 혹은 실수일 수 있다.

print("=" * 50)
print("원의 반지름을 입력하면 그 원의 면적와 둘레를 계산해줍니다.\n")
print("=" * 50)

#반지름 입력
r = (input("원의 반지름 입력 : "))
r = float(r)

# 원 면적 계산
area = r * r * 3.14

# 원의 둘레 계산
circ_fer = 2 * r * 3.14

print()
print(f"원의 면적 : {area}")
print(f"원의 둘레 : {circ_fer}")